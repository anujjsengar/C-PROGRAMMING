#include <stdio.h>
#include <string.h>
#include <math.h>
#include <stdlib.h>
//CONVERSION OF A NUMBER TO DECIMAL,OCTAL,HEXADECIMAL(LOWERACSE) AND HEXADECIMAL(UPPERCASE)
int main() {
    int a;
    scanf("%d",&a);
    printf("%d\n",a);
    printf("%o\n",a);
    printf("%x\n",a);
    printf("%X\n",a);
    return 0;
}
